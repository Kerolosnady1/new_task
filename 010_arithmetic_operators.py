# Arithmetic Operators ( +, -, /, *, %, **, // )

# Addition (+)

print(2 + 2) # 4

numberOne = 2
numberTwo = 2
print(numberOne + numberTwo) # 4

numberThree = 2
numberFour = 2
Sum = numberThree + numberFour
print(Sum) # 4

print(3 + 2 + 5) # 10


# Subtraction (-) => Menus

print(5 - 3) # 2

print(3 - 5) # -2

numberFive = 5
numberSix = 3
print(numberFive - numberSix) # 2

numberSeven = 5
numberEight = 3
subtraction = numberSeven - numberEight
print(subtraction) # 2


# Multiplication (*)

print(5 * 5) # 25

numberNine = 5
numberTen = 5
print(numberNine * numberTen) # 25

numberEleven = 5
numberTwelve = 5
multiplication = numberEleven * numberTwelve
print(multiplication) # 25


# Divion (/)

print(int(12 / 4)) # 3

numberThirteen = 12
numberFourteen = 4
print(int(numberThirteen / numberFourteen)) # 3

print(int(0 / 3)) # 0

# print(3 / 0) # Error

division = numberThirteen / numberFourteen
print(int(division)) # 3


# Modulus (%) Percentage

print(3 % 3) # 3 / 3 = 1 # 0

print(5 % 2) # 5 / 2 = 2.5 # 1 => 6 / 2 = 3

print(5 / 2) # 2.5

numberFifteen = 3
numberSixteen = 3
print(numberFifteen % numberSixteen) # 0


modulus = numberFifteen % numberSixteen
print(modulus) # 0


# Exponent (**)

print(2**2) # 4
print(2 * 2) # 4

print(6**6) # 46656
print(6 * 6 * 6 * 6 * 6 * 6) # 46656

numberEighteen = 3
numberNinteen = 3
print(numberEighteen ** numberNinteen) # 27

exponent = numberEighteen ** numberNinteen
print(exponent) # 27


# Floor Division (//)

print(5 // 3) # 5 / 3 = 1.6 => 1

numberTwenty = 5
numberTwentyOne = 3
print(numberTwenty // numberTwentyOne) # 1

floorDivision = numberTwenty // numberTwentyOne
print(floorDivision) # 1
























