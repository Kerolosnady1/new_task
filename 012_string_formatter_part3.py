# String Formatter Part 3

# Version 3.6 and after:

name = "Kerolos"
age = 20

print(f"My name is {name}, and my age is {age}") # My name is Kerolos, and my age is 20

print(f"Number: {age:.2f}") # Number: 20.00

msg = "Hello my name is Kerolos"

print(f"Long Message: {msg}") # Long Message: Hello my name is Kerolos

print(f"Short Message: {msg:.5s}") # Short Message: Hello
